ymaps.ready(init);

        function init() {
            var points = [
                [53.35439291555429,83.77937251367179],
                [52.41982416119595,85.70202417993164],
                [51.83202432170936,85.77682424340813],
                [51.41023130974189,86.00187406079095],
                [51.95959390309458,85.9601281401366],
                [51.793684649652434,87.26484656079097],
                [51.79364863245807,85.69278738758872],
                [51.99896864616021,84.98101561132813],
                [52.00552973573094,84.62633873291018],
                [51.65998886064143,84.3170835053711],
                [50.31822526405141,87.50649424597167],
                [50.10250247967024,88.36159913226312],
                [52.54436717132143,85.2174067441406]
            ];

            var myMap = new ymaps.Map("map", {
                center: [52.5, 85.5],
                zoom: 7
            });

            for (var i = 0; i < points.length; i++) {
                var point = points[i];
                myMap.geoObjects.add(new ymaps.Placemark(point));
            }

            myMap.geoObjects.add(new ymaps.Polyline(points, {}, {
                strokeColor: "#0000FF",
                strokeWidth: 3,
                strokeOpacity: 0.5
            }));
        }